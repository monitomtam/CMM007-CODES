    <h3>Mount Kilimanjaro, Tanzania</h3>
    <img src= "asset/images/klimanjaro.png" alt="Kilimanjaro Kenya"style="width:100%">
    <p>Mount Kilimanjaro, or just “Kili” to many in the climbing fraternity, 
        is a major tourist attraction in Africa. For those seeking an adventure holiday,
         a trek to the peak of Mount Kilimanjaro is perhaps the epitome of such a vacation.
         Standing at 5,895 meters tall, it takes around 5 to 10 days to summit and returns 
         back to the foot of the tallest mountain in Africa. The climb does not really require
          any specialist climbing equipment, but the right level of fitness is essential.
           Be warned of altitude sickness, this is a prevalent problem among those who attempt the climb.

         Best times to visit: January to March, which is much colder with the potential of snow and June to October. </p>
