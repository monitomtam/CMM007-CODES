    <h3>Traverse the Okavango Delta in Botswana</h3>
    <img src="asset/images/Okavango.png" alt="Okavango Delta" style="width:100%">
    <p>Botswana, in southern Africa, is another legendary safari destination.

        In the northern portion of the country, the Okavango Delta is a top attraction. 
        This large inland river delta floods during the rainy season and becomes a unique
         safari experience. That's when you'll get to explore the river in a dugout canoe, 
         paddling past hippos and crocodiles while keeping your eyes peeled for elephants, 
         giraffe, and other animals who come to the river to drink.
        
        The Moremi Game Reserve covers a large section of the Okavango Delta and is where 
        many upscale safari lodges are located. Maun is the gateway town to the delta, and
         this is a good place to organize more budget-focused canoe and land safaris.
         </p>
