    <h3>Experience Victoria Falls, Zimbabwe and Zambia</h3>
    <img src="asset/images/Victoriafalls.png" alt="Victoria falls" style="width:100%">
    <p>Visit Victoria Falls during the rainy seasons, when the water volume is high, and you'll 
        understand why locals call this impressive waterfall "The Smoke That Thunders."

        The sound of water rushing over rock before dropping into a pool some 300-plus-feet below sounds
         like the roll of thunder, and the mist that spews back up resembles thick smoke. It is an impressive sight.
        
        Protected as a UNESCO World Heritage site, Victoria Falls is on the mighty Zambezi River and creates the border
         between Zimbabwe and Zambia. This waterfall is not only impressive for its dramatic drop but also for its length, 
         which creates a 1.7-kilometer-long curtain of water as it drops.
        
        For the best views of the falls in full, you'll want to explore the Zimbabwe side, where you can follow a walking trail
         to various viewpoints (expect to get drenched when the water volume is high).
        
        On the Zambia side, you can get very close to the falls themselves and, if you're brave enough, even take a dip in Devil's 
        Pool, which literally sits at the top of the falls before they tumble down, down, down. Both sides have lodging options, 
        as well as restaurants.</p>
